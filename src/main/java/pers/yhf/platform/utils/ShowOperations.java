package pers.yhf.platform.utils;

public class ShowOperations {

	public static void printArr(int[] str){
		  int len = str.length;
		  for(int i=0;i<len;i++){
			  System.out.print(str[i]+" ");
		  }
		  System.out.println();
	   }
	  
	public static void printArr(double[] str){
		  int len = str.length;
		  for(int i=0;i<len;i++){
			  System.out.print(str[i]+" ");
		  }
		  System.out.println();
	   }
	
	public static void printArr(String[] str){
		  int len = str.length;
		  for(int i=0;i<len;i++){
			  System.out.print(str[i]+" ");
		  }
		  System.out.println();
	   }
	
}
